# Datatypes in Python
# number - int, float
# text - str
# boolean - bool
# sequence - list, tuple, range
# Set types - set
# Mapping type - dict

print('Datatypes demos')

current_year = 2024
print(type(current_year))

current_year = '2024'
print(type(current_year))

pi = 3.14
print(type(pi))

status = False  #True
print(type(status))

car_list = ['i10','i20']
print(type(car_list))

list_cars = ('i10','i20')
print(type(list_cars))

marks = {"90","80","95"}
print(type(marks))

nation1 = {
    "country": "germany",
    "captial": "berlin",
    "continent": "europe"
}

print(type(nation1))

itr_range = range(6)
print(type(itr_range))
